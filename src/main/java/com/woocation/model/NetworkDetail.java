package com.woocation.model;

/**
 * The Class NetworkDetail.
 */
public class NetworkDetail {
	
	/** The high speed. */
	private Float highSpeed;
	
	/** The coverage. */
	private Float coverage;

	/**
	 * Instantiates a new network detail.
	 */
	public NetworkDetail() {
	}

	/**
	 * Gets the high speed.
	 *
	 * @return the highSpeed
	 */
	public Float getHighSpeed() {
		return highSpeed;
	}

	/**
	 * Sets the high speed.
	 *
	 * @param highSpeed the highSpeed to set
	 */
	public void setHighSpeed(Float highSpeed) {
		this.highSpeed = highSpeed;
	}

	/**
	 * Gets the coverage.
	 *
	 * @return the coverage
	 */
	public Float getCoverage() {
		return coverage;
	}

	/**
	 * Sets the coverage.
	 *
	 * @param coverage the coverage to set
	 */
	public void setCoverage(Float coverage) {
		this.coverage = coverage;
	}
}
