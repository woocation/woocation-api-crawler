package com.woocation.model;

/**
 * The Class Holiday.
 */
public class Holiday {

	/** The holiday name. */
	private String holidayName;
	
	/** The holiday date. */
	private String holidayDate;
	
	/**
	 * Instantiates a new holiday.
	 */
	public Holiday() {
	}

	/**
	 * Gets the holiday name.
	 *
	 * @return the holidayName
	 */
	public String getHolidayName() {
		return holidayName;
	}

	/**
	 * Sets the holiday name.
	 *
	 * @param holidayName the holidayName to set
	 */
	public void setHolidayName(String holidayName) {
		this.holidayName = holidayName;
	}

	/**
	 * Gets the holiday date.
	 *
	 * @return the holidayDate
	 */
	public String getHolidayDate() {
		return holidayDate;
	}

	/**
	 * Sets the holiday date.
	 *
	 * @param holidayDate the holidayDate to set
	 */
	public void setHolidayDate(String holidayDate) {
		this.holidayDate = holidayDate;
	}
}
